define([], function() {
    let userId = null;
    let host = null;
    let courseid = null;

    return {
        setUserId: function(id,hostadd,cid) {
            userId = id;
            host = hostadd;
            courseid = cid;
        },
        getUserId: function() {
            return {
                userid: userId,
                host: host,
                courseid:courseid
            };
        }
    };
});
